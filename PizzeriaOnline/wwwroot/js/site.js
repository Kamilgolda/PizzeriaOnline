function ShowDataEdit() {
    var div = document.getElementById('dane-osobowe');

    if (div.style.display == '') {
        div.style.display = 'none';
    }
    else {
        div.style.display = '';
    }
    var div = document.getElementById('edycja-dane-osobowe');

    if (div.style.display == '') {
        div.style.display = 'none';
    }
    else {
        div.style.display = '';
    }
}